export const MARKET_PHASES = [
  { name: 'Bull Market', symbol: '↑', influence: 'Expansion invites overconfidence. Position for the correction.' },
  { name: 'Bear Market', symbol: '↓', influence: 'Contraction reveals what was real. Buy what you actually believe in.' },
  { name: 'Correction', symbol: '⟳', influence: 'Temporary. The question is whether your timeline is.' },
  { name: 'Consolidation', symbol: '—', influence: 'The market is deciding. You should have decided already.' },
  { name: 'Volatility', symbol: '↯', influence: 'Noise and signal look identical right now. Know your thesis.' },
  { name: 'Recovery', symbol: '↗', influence: 'Early recovery rewards patience. Late recovery rewards caution.' },
  { name: 'Peak', symbol: '△', influence: 'Everyone is confident. That is the data point.' },
  { name: 'Trough', symbol: '▽', influence: 'The bottom is only visible from above it.' },
];

function getMarketPhase(date) {
  const seed = Math.floor(date.getTime() / (1000 * 60 * 60 * 24 * 30));
  return MARKET_PHASES[seed % MARKET_PHASES.length];
}

export function getFinancialReading(signName, date = new Date()) {
  const reading = readings[signName];
  const sign = SIGNS.find(s => s.name === signName);
  const market = getMarketPhase(date);
  return { ...reading, sign, market };
}
